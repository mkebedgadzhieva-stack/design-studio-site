// для поиска по slug
export function findServiceBySlug(slug) {
  for (const cat of servicesCategories) {
    const found = cat.items.find((i) => i.slug === slug);
    if (found) return { ...found, category: cat.title, categorySlug: cat.slug };
  }
  return null;
}
